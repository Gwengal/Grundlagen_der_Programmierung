package Kapitel_03;

/**
 * 
 */
public class Arithmetik {

	/**
	 * 
	 */
	public Arithmetik() {
	}

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int i1;
		
		// 11
		i1 = -3 + 4 * 5 - 6;
		System.out.println(i1);
		
		// 1
		i1 = 3 + 4 % 5 - 6;
		System.out.println(i1);
		
		// -2
		i1 = -3 * 5 % -11 / 2;
		System.out.println(i1);
	}

}
