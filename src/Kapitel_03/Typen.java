package Kapitel_03;

/**
 * 
 */
public class Typen {

	/**
	 * 
	 */
	public Typen() {
	}

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int i1;
		float f1;
		
		i1 = (int)0.9F*2;
		System.out.println(i1);
		
		i1 = 2;
		i1 = 10 * (i1 + 7/2);
		
		System.out.println(i1);
		
		f1 = 10/3;
		System.out.println(f1);
		
		f1 = 10;
		f1 = f1/3;
		System.out.println(f1);
	}

}
